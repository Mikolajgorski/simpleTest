def suma(skladnikX, skladnikY):
    wynik = skladnikX + skladnikY
    return wynik

def roznica(odjemna, odjemnik):
    wynik = odjemna - odjemnik
    return wynik

def iloczyn(czynnikX, czynnikY):
    wynik = czynnikX * czynnikY
    return wynik

def iloraz(dzielna, dzielnik):
    wynik = dzielna / dzielnik
    return wynik

def potega(podstawa, wykladnik):
    wynik = pow(podstawa, wykladnik)
    return wynik
    
def pierwiastek(podstawa, wykladnik):
    wynik = pow(podstawa, 1.0/wykladnik)
    return wynik